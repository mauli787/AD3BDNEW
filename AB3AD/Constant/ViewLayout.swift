import UIKit

extension UICollectionView{
    public func addFlowLayourForCollectionView(colVw: UICollectionView, spacing: CGFloat, scrollType: UICollectionView.ScrollDirection) -> UICollectionViewFlowLayout{
        let layout = UICollectionViewFlowLayout()
        layout.scrollDirection = scrollType
        layout.sectionInset = UIEdgeInsets(top: 0, left: 0, bottom: 0, right: 0)
        layout.minimumLineSpacing = 5
        layout.minimumInteritemSpacing = 0
        
        return layout
         
    }
}


